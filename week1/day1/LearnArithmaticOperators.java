package week1.day1;

public class LearnArithmaticOperators {
	
	
	public static void main(String[] args) {
		
		double a =20.0;
		double b =3.0;
		
		System.out.println(a+b); // to add two numbers
		System.out.println(a-b); // to subtract two numbers
		System.out.println(a*b); // to multiply two numbers
		System.out.println(a/b);  // for division
		System.out.println(a%b);   // to get remainder of two numbers
		
	}

}
